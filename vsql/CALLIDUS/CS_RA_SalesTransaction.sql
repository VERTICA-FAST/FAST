/* Setting timing on**/
\timing

/* SET SESSION AUTOCOMMIT TO OFF; */
\set ON_ERROR_STOP on

CREATE LOCAL TEMP TABLE Start_Time_Tmp ON COMMIT PRESERVE ROWS AS select sysdate st from dual;

/* Inserting values into Audit table  */

INSERT INTO swt_rpt_stg.FAST_LD_AUDT
(
SUBJECT_AREA
,TBL_NM
,LD_DT
,START_DT_TIME
,END_DT_TIME
,SRC_REC_CNT
,TGT_REC_CNT
,COMPLTN_STAT
)
select 'CALLIDUS','CS_RA_SalesTransaction',sysdate::date,sysdate,null,(select count(*) from "swt_rpt_stg"."CS_RA_SalesTransaction") ,null,'N';

Commit;

CREATE LOCAL TEMP TABLE CS_RA_SalesTransaction_stg_Tmp ON COMMIT PRESERVE ROWS AS
(
SELECT DISTINCT * FROM swt_rpt_stg.CS_RA_SalesTransaction)
SEGMENTED BY HASH(SALESTRANSACTIONSEQ) ALL NODES;

CREATE LOCAL TEMP TABLE CS_SalesTransaction_base_Tmp ON COMMIT PRESERVE ROWS AS
(
SELECT DISTINCT SALESTRANSACTIONSEQ,COMPENSATIONDATE FROM swt_rpt_base.CS_SALESTRANSACTION)
SEGMENTED BY HASH(SALESTRANSACTIONSEQ) ALL NODES;

/* Inserting Stage table data into Historical Table */

