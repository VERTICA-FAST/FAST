SELECT ANALYZE_STATISTICS('swt_rpt_base.CS_RA_SalesTransaction');
INSERT /*+Direct*/ INTO swt_rpt_stg.CS_RA_SalesTransaction_Hist SELECT * from swt_rpt_stg.CS_RA_SalesTransaction;
